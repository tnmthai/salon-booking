// === React Context ===
const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => {
    try { return localStorage.getItem('lang') || 'en' } catch { return 'en' }
  })

  const switchLang = (newLang) => {
    setLang(newLang)
    try { localStorage.setItem('lang', newLang) } catch {}
  }

  const t = (key) => {
    return (translations[lang] && translations[lang][key]) || translations.en[key] || key
  }

  return (
    <I18nContext.Provider value={{ t, lang, switchLang }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (ctx) return ctx
  // Fallback when no provider (backward compat)
  return { t, lang: _currentLang, switchLang: setLanguage }
}

// Module-level backward compat
let _currentLang = 'en'
try { _currentLang = localStorage.getItem('lang') || 'en' } catch {}

